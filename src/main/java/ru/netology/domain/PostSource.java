package ru.netology.domain;

public class PostSource {
    private String type;
    private String palatform;
    private String data;
    private String url;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getPalatform() {
        return palatform;
    }

    public void setPalatform(String palatform) {
        this.palatform = palatform;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
